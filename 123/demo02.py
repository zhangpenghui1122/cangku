"""
    闭包
        做法：
            1. 有外有内  -- 有剩菜有人吃
            2. 内使用外　-- 人吃剩菜
            3. 外返回内　-- 人可以重复吃剩菜
        字面意思：封闭内存空间
        价值：外部函数栈帧执行后保存,不释放,供内部函数使用.
"""

def func01():
    a = 10
    def func02():
        print(a)
    # 错误:调用内部函数,返回内部函数的返回值
    # return func02()
    # 返回内部函数
    return func02

# 调用外部函数,返回值是内部函数地址
result = func01()
# 调用内部函数
result()
result()

